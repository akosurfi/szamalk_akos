    private void hatfeletti(String szemszam){
         int szam = 0;
         for (int i = 0; i < 10; i++) {
             szam += (10-i) * Integer.parseInt(Character.toString(szemszam.charAt(i)));
         }
         int modulos = szam%11;
         if (Integer.parseInt(Character.toString(szemszam.charAt(10))) != modulos) {
             helyese.setText("Helytelen személyi szám.");
         }else helyese.setText("Helyes személyi szám.");
    }
    
    private void hatalatti(String szemszam){
         int szam = 0;
         for (int i = 0; i < 10; i++) {
             szam += (i+1) * Integer.parseInt(Character.toString(szemszam.charAt(i)));
             
         }
         int modulos = szam%11;
         if (Integer.parseInt(Character.toString(szemszam.charAt(10))) != modulos) {
             helyese.setText("Helytelen személyi szám.");
         }else helyese.setText("Helyes személyi szám.");
     }
    
