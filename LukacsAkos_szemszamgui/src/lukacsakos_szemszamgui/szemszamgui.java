    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String szemszam = jTextField1.getText();
        String neme = Character.toString(szemszam.charAt(0));
        int mf = Integer.parseInt(neme);
        String evszam1= Character.toString(szemszam.charAt(1));
        String evszam2= Character.toString(szemszam.charAt(2));
        String evszamstring = evszam1 + evszam2;
        int evszam = Integer.parseInt(evszamstring);
        if (evszam > 96 || evszam <17) {
            mikori.setText("Ez egy 97 utáni személyi szám");
            hatfeletti(szemszam);
        }else if (evszam <= 96) {
            mikori.setText("Ez egy 97 előtti személyi szám");
            hatalatti(szemszam);
        }
        if (mf%2==0) {
            milyennemu.setText("Ez egy női személyi.");
        }else{
            milyennemu.setText("Ez egy férfi személyi.");
        }
        

    }//GEN-LAST:event_jButton1ActionPerformed

